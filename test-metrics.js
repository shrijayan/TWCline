console.log('2. Click on the Cline icon in the activity bar');
console.log('3. Click on the metrics button (graph icon) in the top navigation');
console.log('4. The metrics panel should be visible and data should load automatically');
console.log('5. Try switching to another panel (e.g., settings) and back to metrics');
console.log('6. The metrics data should refresh automatically when you return to the panel');
console.log('7. You can also click the refresh button in the metrics panel to manually refresh');

console.log('\nIf you want to test with the VSCode Extension Development Host:');
console.log('1. Open the command palette (Ctrl+Shift+P or Cmd+Shift+P)');
console.log('2. Run the command "Developer: Open Extension Development Host"');
console.log('3. In the new window, click on the Cline icon and test the metrics panel');
